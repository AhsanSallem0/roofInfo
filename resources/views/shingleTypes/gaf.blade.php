    <h3>Product Lines</h3>
    <p>
        GAF sells 15 types of shingles ranging in quality, price, and composition. 
    </p>
    <p>
        Some brand names they feature are Sentinel®, Timberline®, Camelot™, 
        Grand Canyon™, Royal Sovereign®, Stateline®, Grand SLATE™, 
        Grand Sequoia®, Country Mansion®, Country Estate™, and Marquis®. 
    </p>

    <p>
        Various shingles are laminated, have the appearance of a slate roof, look like 
        wood shakes, or are dimensional, meaning they give a shadowed appearance.
    </p>
    <h3>GAF's History</h3>
    <p>
        GAF Materials Corporation (GAFMC) was founded in 1886 and is now one of North 
        America's largest roofing product manufacturers. In 2007, GAF merged with 
        <a href="/shingles/brands/elk.html">Elk</a>, although the new company will continue to
        market both brands of shingles. Corporate headquarters are located in Wayne, NJ. 
    </p>
    <h3>Shingle Warranties</h3>
    <p>
        GAF shingles are covered by warranties that depend on the quality of the shingle. 
        The more highly rated the shingle, the higher the level of coverage. 
    </p>
    <p>
        The areas covered by the warranties are:
        </p><ul>
            <li>Materials delivered defect-free from manufacturer and installed per their 
                recommendations; coverage varies from five to 50 years</li>
            <li>Entire roofing system is covered under two most extensive plans</li>
            <li>Installation labor for replacement shingles varies from 5 years to lifetime, 
                depending on the length of time the shingle itself is guaranteed for</li>
            <li>Workmanship is guaranteed for 12 years maximum; under basic warranty, no 
                coverage is provided</li>
            <li>40-point factory inspection offered under highest warranty</li>
            <li>Warranty is transferable under all plans.</li>
        </ul>
    <p></p>
    <p>
        The top GAF warranty is called the "Golden Pledge" and is applicable to all GAF 
        "Lifetime" shingles.
