    <h3>Summary</h3>
    <p>
        Timberline architectural shingles are made to look like wood shakes. They are 
        rated Class A for <a href="/definitions/fire_ratings.html">fire</a> and <a href="/definitions/wind-ratings.html">wind</a>
        (the top safety ratings available). They make several models:
        </p><ul>
            <li>Grand Timberline™</li> 
            <li>Timberline Select® 40</li>
            <li>Timberline 30</li>
            <li>Timberline Ultra®</li>
        </ul>
    <p></p>
 
    <p>
        Patented features include Micro Weave® core to lengthen life and Dura Grip®
        seal (special installation) to protect against high wind blow-off. Timberline 
        shingles vary in thickness, appearance, and choice of color by brand name. 
    </p>
    <h3>History</h3>
    <p>
        Timberline is a brand of residential shingles owned by 
        <a href="/shingles/brands/gaf.html">GAF</a>.  GAF Materials Corporation was founded 
        in 1886. They are now among the largest commercial and residential roofing product 
        manufacturers in North America.  
    </p>
    <h3>Warranty</h3>
    <p>
        GAF offers three warranty choices on their Timberline shingles. Coverage is 
        dependent on warranty cost. The areas covered by the warranties are:
        </p><ul>
            <li>Materials delivered defect-free from the manufacturer and installed per 
                their recommendations; coverage varies from five to 50 years</li>
            <li>Entire roofing system is covered under two most extensive plans</li>
            <li>Installation labor varies from five years to lifetime, dependent on 
                whatever length the shingle itself is guaranteed for</li>
            <li>Workmanship is guaranteed for 12 years maximum; under basic warranty, 
                no coverage is provided</li>
            <li>40-point factory inspection offered under highest warranty</li>
            <li>Warranty is transferable under all plans.</li>
        </ul>
    <p></p>
    <p>
        The top GAF warranty is called the "Golden Pledge" and is applicable to all 
        Timberline shingles.
