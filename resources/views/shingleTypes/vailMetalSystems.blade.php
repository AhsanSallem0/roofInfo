    <h1>Vail Metal Shingles</h1>
    
<a href="/perl/review.pl?brand_id=252&amp;equip_type=shingle">Write a review of Vail shingles</a>

    <h3>Summary</h3>
    <p>
        Vail Metal Systems is a Colorado-based company that makes <a href="/shingles/metal.html">metal shingles</a>
        called the Riva Series.  These shingles are available in either 16-ounce solid copper (called the Riva Classic) or 
        26-gauge coated Galvalume steel (a corrosion-resistant galvanized steel shingle called the 
        Riva Select). The Vail metal shingles are crafted from a single metal shingle panel that is 
        folded to give the appearance of several dimensional shingles. 
    </p>
 
    <p>
        Steel shingles have a baked-on paint finish in a selection of 25 different colors. Copper 
        shingles are available in the natural metallic finish, which changes in appearance as the 
        shingles age, taking on a warm patina.
    </p>
    <p>
        Vail metal shingles are approximately 30 times lighter than concrete roof tiles.
    </p>
    <p>
        Underwriter Laboratories has given the Vail shingles a Class A   
        <a href="/definitions/fire_ratings.html">fire resistance rating</a> as well as a 110-mph wind
        resistance. The Riva Series (Vail Titan Select) has attained the Metro-Dade County [Florida] 
        Product Control Approval.
    </p>
    <h3>History</h3>
    <p>
        Vail Metal Systems was established by custom roofing contractors that wanted to meet the needs 
        of customers in Colorado's Rocky Mountain property owners. These owners experienced extreme 
        ice and snow loads upon their roofs, but needed roofing that not only were strong enough to 
        handle that but also met aesthetic codes. The shingles are formed in a single square sheet, 
        making it easy for roofing contractors to estimate the amount of roofing needed. 
    </p>
    <p>
        Vail Metal Systems shingles were marketed by Owens Corning under their MiraVista Designer 
        Metals series until 2000. As of 2004, Custom-Bilt Metals began marketing Vail Metal 
        Systems as Vail Majestic Copper and Vail Titan Select.
    </p>
    <h3>Warranty</h3>
    <p>
        Both metal shingles carry a 50-year full warranty that can be transferred one time. The paint 
        on the metal shingles is guaranteed not to chip, peel, or fade for 30 years.
    </p>
    <p>
        Review your product warranty carefully to make sure you understand the terms.
