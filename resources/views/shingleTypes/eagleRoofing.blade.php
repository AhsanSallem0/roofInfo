    <h1>Eagle Roofing</h1>
    
<a href="/perl/review.pl?brand_id=5513&amp;equip_type=shingle">Write a review of Eagle Roofing shingles</a>

    <p>
        California-based Eagle Roofing Products offers tile roofing products of varying styles and colors that copy the traditional designs of adobe, 
	as well as cedar and oak shakes under the brand name Ce Dur. Eagle Roofing Products also offers Cool Roof Tile products, which are designed 
	to reflect away sunlight and heat and thus keep homes' interiors cool. 
    </p>
 
    <p>
	Eagle's tiles are manufactured of extruded cement and come in a number of terra cotta colors with varying shades of tan, burnt orange, cayenne 
	red, cream, pink and other colors. The company's Artisan line of concrete tiles feature a single base color overlaid with random accent colors 
	to achieve a rustic look where no two tiles look alike. The shakes are manufactured from lightweight synthetic materials and mimic the aesthetics 
	of oak and cedar. They are available in shades of brown, gray and green. 
    </p>
    <h3>Company History</h3>
    <p>
        Eagle was founded in 1989 by Burlingame Industries to manufacture extruded concrete roofing tiles. According to the company website: "Eagle 
	Roofing Products has prided itself on building strong relationships with customers of all sizes by listening and responding to their requests." 
	The company started with just one high-pressure concrete extruding machine. It now operates plants across the United States, manufacturing concrete 
	and synthetic tiles. 
    </p>
    <p>
	The company is a division of Burlingame Industries, a family-owned and privately-held company that has been in the roofing business for more 
	than 40 years. It employs approximately 400 people, including production, administrative and sales staff. Its main office is located in Rialto, Calif. 
    </p>
