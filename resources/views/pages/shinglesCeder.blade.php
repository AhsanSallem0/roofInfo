    <h1>Cedar Roofing Shingles</h1>
    <p>
	Cedar shingles have been the material of choice for many roofs, especially in the northwestern US. 
	Before the 1950s there was plenty of cedar heartwood, the part closest to the center of the tree. 
	It was hard, could be cut straight, and had natural toxins against insects and rot. It was considered 
	the roofing material of choice for people who could afford it.
    </p>

    <h3>How are Cedar Shingles made?</h3>
    <p>
	The trees are harvested by loggers and delivered to wood mills. Each log is cut into rounds, 
	each one usually measuring about two feet long. The rounds are split into v-shaped blocks and hand 
	split into the cedar shakes used for roofing and siding. The shakes are sorted into bundles and 
	sent to manufacturers. The remaining shavings and pieces are sometimes bought by companies that 
	use them in composite shingles.
    </p>
    <p>
	Some roofing makers pre-treat each shake with preservative and fire-retardant. Others recommend 
	that the roofing installer or homeowner take these steps. 
    </p>
    <h3>How long do Cedar shingles last?</h3>
    <p>
	Nowadays, not very long: some roofs start deteriorating within six years. The good news for people, 
	who don't mind imitations, is that there are some quality look-alike products made of composites. 
	Treating with a wood preservative every two or so years, and replacing damaged shingles will prolong 
	the life of the roof. Some well-cared for cedar shingled roofs can last for at least 20 years. 
    </p>
    <h3>Common problems with Cedar Shingles</h3>
    <p>
	Although cedar shingles are traditionally made from the heartwood, today's shingles are often taken 
	closer to the outside of the tree
	This wood is called "sap wood" and is inferior to heartwood in several ways: it doesn't cut in a 
	straight grain and it hasn't developed the natural toxins of heartwood.  This younger cedar is subject 
	to fire and wood rot. Also, most roofs aren't pitched as steeply, so less water runs off.
    </p>
    <p>
	Fire danger is severe. It isn't wise to have a cedar roofed house in an area surrounded by trees. 
	No matter where the house is, cedar roofs should be treated with a fire retardant. 
