    <p>
	Buy only cedar shingles that are number 1, blue label rated. Examine each shingle in a bundle as 
	inferior grades are often mixed in. Quality and properly cared for cedar shingles can last for 
	30 years, withstand hail storms, and winds up to 130 mph.
