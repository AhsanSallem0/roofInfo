    <h1>Copper Roofing Shingles</h1>
    <p>
	Copper is a strong, resilient and (since it can be recycled) environmentally-friendly metal.
	New copper shingles often contain some recycled copper in them. These factors cause many people to choose it as a roofing material.  
	In terms of price they fall in the middle between the low end asphalt and high end slate and cedar. Copper changes color naturally 
	as it ages due to chemical reactions between the metal and the atmosphere. After about ten years, copper shingles become a light 
	green color. Upkeep of a copper roof is minimal. It doesn't dry out or crack, and no extra roofing reinforcement is needed.
    </p>

    <h3>How are Copper Shingles made?</h3>
    <p>
	Copper is melted and shaped into slabs of various sizes. These are sent to a rolling mill to be made into coils. The standard coil 
	weights are twelve to sixteen ounces. The shingles are either cut individually or into long sheets. There are interlocking copper shingle systems. 
    </p>
    <h3>How long do Copper shingles last?</h3>
    <p>
	Manufacturers claim that they'll last 100 to 300 years. 
    </p>
    <h3>Common problems with Copper Shingles</h3>
    <p>
	There are very few problems with copper used in roofing. If there is a combination of materials, corrosion may occur when water runs 
	off from an acidic material above the copper. 
